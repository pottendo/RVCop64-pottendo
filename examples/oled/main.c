static void fail(const char *msg)
{
  printf("%s\r\n", msg);
  for(;;)
    ;
}

int main(void)
{
#ifdef CONFIG_CPU_HAS_INTERRUPT
	irq_setmask(0);
	irq_setie(1);
#endif
	uart_init();

	i2c_reset();
	if (!ssd1306_init() || !ssd1306_clear())
	  fail("Unable to init SSD1306");

	if (!ssd1306_hbitmap(0, 0, sizeof(logo)/sizeof(logo[0]),
			     sizeof(logo[0])/sizeof(logo[0][0]), &logo[0][0]))
	  fail("Failed to draw logo");

	if (!ssd1306_display_on())
	  fail("Unable to enable display");

	if (!ssd1306_hscroll(false, 0, 3, 5))
	  fail("Unable to scroll");

	return 0;
}
